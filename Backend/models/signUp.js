const mongoose = require('mongoose');

const signup = mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const SignUp = mongoose.model('SignUp', signup);

module.exports = SignUp;
